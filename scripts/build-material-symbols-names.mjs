import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const pluginRoot = path.resolve(scriptDir, '..');
const codepointsPath = path.join(pluginRoot, 'libs/material-symbols/codepoints');
const outputPath = path.join(pluginRoot, 'modules/tavern/shared/material-symbols-names.ts');

function parseCodepoints(source) {
    return [...new Set(source
        .split(/\r?\n/)
        .map((line) => line.trim().split(/\s+/)[0] || '')
        .filter(Boolean))]
        .sort((left, right) => left.localeCompare(right));
}

const names = parseCodepoints(fs.readFileSync(codepointsPath, 'utf8'));
const body = [
    '// Generated by scripts/build-material-symbols-names.mjs. Do not edit by hand.',
    '',
    'export const MATERIAL_SYMBOL_NAMES = [',
    ...names.map((name) => `    ${JSON.stringify(name)},`),
    '] as const;',
    '',
    'export type MaterialSymbolName = typeof MATERIAL_SYMBOL_NAMES[number];',
    '',
].join('\n');

fs.writeFileSync(outputPath, body, 'utf8');
console.log(`Material Symbols names written to ${path.relative(pluginRoot, outputPath)} (${names.length} names)`);
